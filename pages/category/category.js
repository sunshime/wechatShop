  setFoodListAreaHeight() {
    let query = wx.createSelectorQuery();
    let that = this;
    //分类栏的高度
    query.select('.rt-title').boundingClientRect(function (rect) {
      console.log('rect.height--->', rect.height)
      that.setData({
        eleCateTitleHeight: rect.height
      })
    }).exec();
    //商品item的高度
    query.select('.ct-item').boundingClientRect(function (rect) {
      console.log('rect.height--111->', rect.height)
      that.setData({
        eleFoodHeight: rect.height
      })
    }).exec();

    //把商品列表每个分类的区间高度计算，并放进数组
    //上面获取元素的高度可能不是同步的，所以把下面的放在setTimeout里面
    let foodAreaHeight = [0]
    let heightCount = 0
    setTimeout(() => {
      this.data.categoryRightList.forEach((item, index) => {
        //console.log(item.items.length * this.data.eleFoodHeight);
        item.subList.forEach(items=>{
          heightCount += items.length * this.data.eleFoodHeight + this.data.eleCateTitleHeight
          foodAreaHeight.push(heightCount)
        })
      })
      this.setData({
        foodAreaHeight
      })
    }, 100)

  },
	/**
	 * 滚动商品的时候
	 * @param {*} event 
	 */
  foodListScrolling(event) {
    let scrollTop = event.detail.scrollTop
    let foodAreaHeight = this.data.foodAreaHeight
    console.log(scrollTop);
    foodAreaHeight.forEach((item, index) => {
      if (scrollTop >= foodAreaHeight[index] && scrollTop < foodAreaHeight[index + 1]) {
        this.setData({ cateListActiveIndex: index })
      }
    })
  },

